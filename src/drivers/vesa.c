void vesa_draw_char(int x, int y, char c, unsigned int color, unsigned int bg_color) {
    // Pastikan karakter berada dalam jangkauan array font
    unsigned char idx = (unsigned char)c;
    if (idx >= 128) return;

    for (int row = 0; row < 16; row++) {
        unsigned char line = font_8x16[idx][row];
        for (int col = 0; col < 8; col++) {
            // Memeriksa bit dari MSB ke LSB (kiri ke kanan)
            if (line & (0x80 >> col)) {
                vesa_put_pixel(x + col, y + row, color);
            } else if (bg_color != 0) {
                vesa_put_pixel(x + col, y + row, bg_color);
            }
        }
    }
}


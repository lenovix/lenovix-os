void vesa_draw_char(int x, int y, char c, unsigned int color) {
    unsigned char uc = (unsigned char)c;
    if (uc > 127) return;

    for (int row = 0; row < 16; row++) {
        unsigned char bitmask = font_8x16[uc][row];
        for (int col = 0; col < 8; col++) {
            if (bitmask & (1 << (7 - col))) {
                vesa_put_pixel(x + col, y + row, color);
            }
        }
    }
}

void vesa_draw_string(int x, int y, const char *str, unsigned int color) {
    int cur_x = x;
    while (*str) {
        vesa_draw_char(cur_x, y, *str, color);
        cur_x += 8;
        str++;
    }
}